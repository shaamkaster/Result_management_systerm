    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (tablest.getSelectedRowCount() == 1) {
        try {
            int ass3mark = Integer.parseInt(txtass3.getText());

            // Validate mark is between 0 and 100
            if (ass3mark < 0 || ass3mark > 100) {
                JOptionPane.showMessageDialog(this, "Please enter a mark between 0 and 100");
                return;
            }

            String id = lblstidshow.getText();
            String query = "UPDATE `acadamin` SET `exam3`=? WHERE `stId`=?";
            PreparedStatement pst = con.prepareStatement(query);
            pst.setInt(1, ass3mark);
            pst.setString(2, id);
            pst.execute();
            JOptionPane.showMessageDialog(this, "Mark updated successfully");

            settable("SELECT * FROM `acadamin`");
            clear();

        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Invalid input. Please enter a number.");
        } catch (Exception e) {
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "An error occurred while updating the mark.");
        }
    } else {
        JOptionPane.showMessageDialog(this, "Please select one student");
    }

        
    }//GEN-LAST:event_jButton5ActionPerformed

