    private ArrayList<GSmethod> getstresult(String query) {
        ArrayList<GSmethod> temp = new ArrayList<>();
        Statement st;
        try {
            st = con.createStatement();
            rs = st.executeQuery(query);
            GSmethod marks;
            while (rs.next()) {
                marks = new GSmethod(rs.getInt(1), rs.getString(2), rs.getInt(3), rs.getString(4), rs.getString(5), rs.getInt(6), rs.getInt(7), rs.getInt(8));
                temp.add(marks);

            }
        } catch (Exception e) {
        }
        return temp;
    }

    private void settable(String query) {
        strmodel.setRowCount(0);
        sttable = getstresult(query);
        for (int i = 0; i < sttable.size(); i++) {
            String e = sttable.get(i).getStName();
            String f = sttable.get(i).getStId();
            int g = sttable.get(i).getExam1();
            int h = sttable.get(i).getExam2();
            int j = sttable.get(i).getExam3();
            strmodel.addRow(new Object[]{e, f, g, h, j});
        }
    }

