    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int id = Integer.parseInt(hideid.getText());
        String name = txtname.getText();
        int age = Integer.parseInt(txtage.getText());
        String nic = txtnic.getText();
        String tp = txttp.getText();
        try {
            String query = "UPDATE `acadamin` SET `stName`=?,`stAge`=?,`stId`=?,`tpNo`=? WHERE `autoId`=?";
            PreparedStatement pst;
            pst = con.prepareStatement(query);
            pst.setString(1, name);
            pst.setInt(2, age);
            pst.setString(3, nic);
            pst.setString(4, tp);
            pst.setInt(5, id);

            pst.execute();
            JOptionPane.showMessageDialog(this, "Edit Sucess");
            txtname.setText("");
            txtage.setText("");
            txtnic.setText("");
            txttp.setText("");
            settable("SELECT * FROM `acadamin`");

        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

